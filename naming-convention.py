## Naming Conventions

# snake_case
    # Python standard naming convention
    # always lowercase
    # always underscored between words

# camelCase
    # JavaScript naming convention
    # first word lowercase
    #capitalize the firts letter of each word that follows

# PascalCase
    # similar to camelCase but with every first letter of each word capitalized

# kebab-case
    # every-word-is-serparated-by-a-dash

# ALL CAPS
    # represent a variable that will never change

